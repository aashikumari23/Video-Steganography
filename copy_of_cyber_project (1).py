def get_video_chunks(file_path, chunk_size=8, max_frames=None):
    """
    Generator that yields 4D color chunks (F, H, W, 3).
    Ensures dimensions are even by trimming (no padding).
    """
    cap = cv2.VideoCapture(file_path)
    total_count = 0

    while True:
        chunk = []
        for _ in range(chunk_size):
            if max_frames and total_count >= max_frames:
                break
            ret, frame = cap.read()
            if not ret:
                break

            h, w, _ = frame.shape


            frame = frame[:h - (h % 2), :w - (w % 2), :]

            chunk.append(frame)
            total_count += 1

        if not chunk:
            break


        if len(chunk) % 2 != 0:
            chunk.pop()

        if not chunk:
            break

        yield np.array(chunk, dtype=np.uint8)

    cap.release()


def compress_video(video_array):
    raw_data = video_array.tobytes()
    compressed = zlib.compress(raw_data, level=9)
    return np.unpackbits(np.frombuffer(compressed, dtype=np.uint8))


def decompress_video(bits, shape):
    byte_data = np.packbits(bits).tobytes()
    decompressed = zlib.decompress(byte_data)
    return np.frombuffer(decompressed, dtype=np.uint8).reshape(shape)

def lwt_1d(data):
    data = data.astype(np.int32)
    evens, odds = data[0::2], data[1::2]
    d = odds - evens
    s = evens + (d // 2)
    return np.concatenate([s, d])

def ilwt_1d(data):
    half = data.shape[0] // 2
    s, d = data[:half], data[half:]
    evens = s - (d // 2)
    odds = d + evens
    recon = np.empty_like(data)
    recon[0::2], recon[1::2] = evens, odds
    return recon


def transform_3d_chunk(volume, forward=True):
    """Now supports both grayscale (F,H,W) and color (F,H,W,3)"""

    func = lwt_1d if forward else ilwt_1d


    if volume.ndim == 3:
        out = volume.copy().astype(np.int32)

        for y in range(out.shape[1]):
            for x in range(out.shape[2]):
                out[:, y, x] = func(out[:, y, x])

        for f in range(out.shape[0]):
            for x in range(out.shape[2]):
                out[f, :, x] = func(out[f, :, x])

        for f in range(out.shape[0]):
            for y in range(out.shape[1]):
                out[f, y, :] = func(out[f, y, :])

        return out


    elif volume.ndim == 4:
        F, H, W, C = volume.shape
        out = np.zeros_like(volume, dtype=np.int32)

        for c in range(C):
            channel = volume[:, :, :, c].astype(np.int32)

            for y in range(H):
                for x in range(W):
                    channel[:, y, x] = func(channel[:, y, x])

            for f in range(F):
                for x in range(W):
                    channel[f, :, x] = func(channel[f, :, x])

            for f in range(F):
                for y in range(H):
                    channel[f, y, :] = func(channel[f, y, :])

            out[:, :, :, c] = channel

        return out

