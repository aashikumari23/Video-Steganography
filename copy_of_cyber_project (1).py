def run_steganography_pipeline(cover_path, secret_path, stego_path, recovered_path, chunk_size=8):

    cap_s = cv2.VideoCapture(secret_path)
    s_frames = []
    for _ in range(30):
        ret, f = cap_s.read()
        if not ret: break

        h, w, _ = f.shape
        f = f[:h-(h%2), :w-(w%2), :]

        s_frames.append(f)

    cap_s.release()

    secret_vid = np.array(s_frames, dtype=np.uint8)

    if len(secret_vid) % 2 != 0:
        secret_vid = secret_vid[:-1]

    secret_shape = secret_vid.shape


    compressed_bytes = secret_vid.tobytes()
    secret_bits = np.unpackbits(np.frombuffer(compressed_bytes, dtype=np.uint8))
    bit_len = len(secret_bits)


    bits_per_chunk_tracker = []
    bit_ptr = 0
    CHUNK_SIZE = chunk_size

    cap_c = cv2.VideoCapture(cover_path)
    total_f = int(cap_c.get(cv2.CAP_PROP_FRAME_COUNT))
    cap_c.release()
    total_chunks = total_f // CHUNK_SIZE

    print(f"Embedding {bit_len} bits into {total_chunks} chunks...")

    writer = None
    first_write = True

    for cover_chunk in tqdm(get_video_chunks(cover_path, CHUNK_SIZE), total=total_chunks, desc="Embedding"):

        cover_chunk = np.clip(cover_chunk, 5, 250).astype(np.uint8)


        coeffs = transform_3d_chunk(cover_chunk, forward=True)

        flat = coeffs.flatten()

        chunk_cap = int(len(flat) * 0.85)
        bits_to_hide = min(bit_len - bit_ptr, chunk_cap)
        bits_per_chunk_tracker.append(bits_to_hide)

        if bits_to_hide > 0:
            segment = secret_bits[bit_ptr : bit_ptr + bits_to_hide]
            flat[-bits_to_hide:] = (flat[-bits_to_hide:] & ~1) | segment
            bit_ptr += bits_to_hide

        stego_coeffs = flat.reshape(coeffs.shape)

        stego_chunk = transform_3d_chunk(stego_coeffs, forward=False)
        stego_chunk = np.clip(stego_chunk, 0, 255).astype(np.uint8)

        if first_write:
            f, h, w, c = stego_chunk.shape
            writer = cv2.VideoWriter(stego_path, cv2.VideoWriter_fourcc(*'FFV1'), 30, (w, h), True)  # ✅ COLOR
            first_write = False

        for frame in stego_chunk:
            writer.write(frame)

    writer.release()


    print("\n--- Starting Extraction ---")

    extracted_bits_list = []
    bits_left_to_extract = bit_len

    chunk_gen = get_video_chunks(stego_path, CHUNK_SIZE)

    for i, stego_chunk in enumerate(tqdm(chunk_gen, desc="Extracting")):

        coeffs = transform_3d_chunk(stego_chunk, forward=True)
        flat = coeffs.flatten()

        chunk_cap = int(len(flat) * 0.85)
        bits_to_pull = min(bits_left_to_extract, chunk_cap)

        if bits_to_pull > 0:
            segment = flat[-bits_to_pull:] & 1
            extracted_bits_list.append(segment)
            bits_left_to_extract -= bits_to_pull

        if bits_left_to_extract <= 0:
            break

    if bits_left_to_extract > 0:
        print(f"CRITICAL ERROR: Missing {bits_left_to_extract} bits.")
        return

    full_extracted_bits = np.concatenate(extracted_bits_list)[:bit_len]
    recovered_bytes = np.packbits(full_extracted_bits)


    recovered_vid = recovered_bytes.reshape(secret_shape)


    f_s, h_s, w_s, c_s = recovered_vid.shape
    rec_writer = cv2.VideoWriter(recovered_path, cv2.VideoWriter_fourcc(*'FFV1'), 30, (w_s, h_s), True)

    for frame in recovered_vid:
        rec_writer.write(frame.astype(np.uint8))

    rec_writer.release()

    print(f"Success! Saved to {recovered_path}")

